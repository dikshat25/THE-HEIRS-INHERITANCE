  factory Recipe.fromJson(Map<String, dynamic> json) {
    return Recipe(
      recipeId: json['recipe_id'] as String,
      name: json['name'] as String,
      description: json['description'] as String,
      cuisine: json['cuisine'] as String,
      course: json['course'] as String,
      diet: json['diet'] as String,
      ingredientsName: List<String>.from(json['ingredients_name']),
      ingredientsQuantity: List<String>.from(json['ingredients_quantity']),
      prepTime: json['prep_time'] as int,
      cookTime: json['cook_time'] as int,
      instructions: json['instructions'] as String,
      imageURL: json['image_url'] as String,
      totalTime: json['total_time'] as int,
      recipeTags: List<String>.from(json['recipe_tags']),
      difficultyLevel: json['difficulty_level'] as String,
      ingredientCategory: json['ingredient_category'] as String,
      isFavorited: json['isFavorited'] as bool? ?? false,
    );
  }

  Map<String, dynamic> toJson() {
    return {
