      'prep_time': prepTime,
      'cook_time': cookTime,
      'instructions': instructions,
      'image_url': imageURL,
      'total_time': totalTime,
      'recipe_tags': recipeTags,
      'difficulty_level': difficultyLevel,
      'ingredient_category': ingredientCategory,
      'isFavorited': isFavorited,
    };
  }

